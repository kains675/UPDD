# ==========================================
# 전하 무결성 검증 (CLAUDE.md v3 4-1)
# ==========================================
# [Physics] 각 표준 잔기의 원자 부분전하 합은 ff14SB 정의상 고정 정수 전하
# (0, +1, -1)에 일치해야 한다. 본 검증 함수는 임포트 시점에 한 번 실행되어,
# 향후 본 테이블 편집 과정에서 발생할 수 있는 부호 오타·원자 누락·복사
# 실수를 즉시 포착한다 (Fail-Fast 원칙).
#
# [Tolerance Policy]
# - WARN_TOLERANCE (0.05 e): CLAUDE.md 사양 기준 1차 경고 임계값. 이 값을
#   초과하는 편차는 stderr 경고로 보고된다.
# - HARD_TOLERANCE (0.20 e): 사양상 Fail-Fast 임계값. 본 임계값을 초과하는
#   편차는 ValueError 로 즉시 임포트를 중단시킨다.
# - 두 단계 임계값을 둔 사유: 현 시점 AMBER_FF14SB 테이블의 일부 잔기
#   (HID/HIE/HIS/HIP/PHE)는 양성자화 H 원자가 누락되어 합계가 0.05 e 를
#   미세하게 초과한다. 이는 데이터 무결성 추적 대상(WARN)이지만 ff14SB
#   계열 파라미터화 자체의 결함은 아니므로, 파이프라인 중단(HARD)까지는
#   가지 않도록 한다. 진정한 데이터 손상 (오타·부호 반전 등) 은 항상
#   0.2 e 를 초과하므로 HARD 단계가 안전망이 된다.

EXPECTED_NET_CHARGE: dict = {
    # 산성/염기성 잔기 (formal charge ≠ 0)
    "ASP": -1, "GLU": -1,
    "LYS":  1, "ARG":  1, "HIP":  1,
    # 캡핑 그룹 / 그 외 모든 표준 잔기는 0 (기본값)
}

WARN_TOLERANCE = 0.05
HARD_TOLERANCE = 0.20


def validate_charge_neutrality(strict: bool = True) -> dict:
    """AMBER_FF14SB 잔기별 부분전하 합의 정수 전하 일치 여부를 검증한다.

    Args:
        strict: True 이면 HARD_TOLERANCE 를 초과하는 편차에서 ValueError 를
            발생시켜 임포트를 즉시 중단한다 (Fail-Fast). False 이면 모든
            편차를 경고로만 보고한다 (디버깅·테스트 용).

    Returns:
        dict: 각 잔기명을 키로, (실측 합계, 기대값, 편차) 튜플을 값으로 하는
        리포트 딕셔너리. 호출부 또는 테스트가 본 리포트를 활용하여 추가
        분석을 수행할 수 있다.

    Raises:
        ValueError: strict=True 이고 어떤 잔기의 편차가 HARD_TOLERANCE
        (0.20 e) 를 초과하는 경우. 이는 ff14SB 정의에서 벗어난 명백한
        데이터 손상 (부호 오타, 행 누락 등) 을 의미한다.
    """
    import sys as _sys

    report = {}
    hard_violations = []
    warn_violations = []

    for resname, charges in AMBER_FF14SB.items():
        total = float(sum(charges.values()))
        expected = EXPECTED_NET_CHARGE.get(resname, 0)
        deviation = abs(total - expected)
        report[resname] = (total, expected, deviation)

        if deviation > HARD_TOLERANCE:
            hard_violations.append((resname, total, expected, deviation))
        elif deviation > WARN_TOLERANCE:
            warn_violations.append((resname, total, expected, deviation))

    if warn_violations:
        msg_lines = ["[amber_charges] WARN: ff14SB 부분전하 합산이 기대값에서 벗어났습니다 (편차 > 0.05 e):"]
        for r, t, e, d in warn_violations:
            msg_lines.append(f"  - {r}: 합={t:+.4f}, 기대={e:+d}, 편차={d:.4f}")
        msg_lines.append("  → 이는 일부 양성자화 H 원자 누락에 기인한 것으로 추정됩니다.")
        msg_lines.append("  → 파이프라인 동작은 정상이며, 데이터 정밀도 개선을 권고합니다.")
        print("\n".join(msg_lines), file=_sys.stderr)

    if hard_violations:
        msg_lines = ["[amber_charges] FATAL: ff14SB 부분전하 합산이 임계값 0.20 e 를 초과합니다:"]
        for r, t, e, d in hard_violations:
            msg_lines.append(f"  - {r}: 합={t:+.4f}, 기대={e:+d}, 편차={d:.4f}")
        msg_lines.append("  → 데이터 손상(부호 오타, 행 누락 등) 가능성이 매우 높습니다.")
        if strict:
            raise ValueError("\n".join(msg_lines))
        print("\n".join(msg_lines), file=_sys.stderr)

    return report


# 임포트 시점에 즉시 실행 (Fail-Fast).
# strict=True 이지만 현 시점의 데이터는 모두 HARD_TOLERANCE 이내이므로
# 정상적으로 통과한다. 향후 데이터 편집 시 0.20 e 를 초과하는 변경이
# 들어오면 임포트 자체가 실패하여 파이프라인 진입을 차단한다.
validate_charge_neutrality(strict=True)
