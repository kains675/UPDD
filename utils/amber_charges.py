# [Backwards-Compat Alias]
# CLAUDE.md v3 4-2 항목에 따라 정식 이름은 BACKBONE_FALLBACK_CHARGES 이다.
# 기존 호출부(`from amber_charges import _BACKBONE_FALLBACK`)와의 호환성을
# 유지하기 위해 alias 를 함께 노출하며, 신규 코드는 BACKBONE_FALLBACK_CHARGES
